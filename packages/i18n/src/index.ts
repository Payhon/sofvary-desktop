export function normalizeLocale(value: unknown): Locale {
  if (typeof value !== "string") {
    return DEFAULT_LOCALE;
  }
  const normalized = value.trim().replace("_", "-");
  if (supportedLocaleSet.has(normalized)) {
    return normalized as Locale;
  }
  const lower = normalized.toLowerCase();
  if (lower === "zh" || lower.startsWith("zh-")) {
    return "zh-CN";
  }
  if (lower === "en" || lower.startsWith("en-")) {
    return "en";
  }
  return DEFAULT_LOCALE;
}

export function resolveLocaleFromHeader(header: string | string[] | undefined): Locale {
  const rawHeader = Array.isArray(header) ? header.join(",") : header;
  if (!rawHeader) {
    return DEFAULT_LOCALE;
  }

  const candidates = rawHeader
    .split(",")
    .map((part) => {
      const [tag, ...attributes] = part.trim().split(";");
      const q = attributes
        .map((attribute) => attribute.trim())
        .find((attribute) => attribute.startsWith("q="));
      return {
        tag: tag ?? "",
        quality: q ? Number.parseFloat(q.slice(2)) || 0 : 1
      };
    })
    .filter((candidate) => candidate.tag && candidate.quality > 0)
    .sort((left, right) => right.quality - left.quality);

  for (const candidate of candidates) {
    const normalized = normalizeLocale(candidate.tag);
    const lower = candidate.tag.toLowerCase();
    if (normalized !== DEFAULT_LOCALE || lower === "en" || lower.startsWith("en-")) {
      return normalized;
    }
  }

  return DEFAULT_LOCALE;
}

export function createTranslator(localeInput: unknown, namespace?: TranslationNamespace) {
  const locale = normalizeLocale(localeInput);
  return (key: string, params: TranslationParams = {}, fallback?: string): string =>
    translate(key, locale, params, namespace, fallback);
}

export function translate(
  key: string,
  localeInput: unknown,
  params: TranslationParams = {},
  namespace?: TranslationNamespace,
  fallback?: string,
): string {
  const locale = normalizeLocale(localeInput);
  const namespaces: TranslationNamespace[] = namespace
    ? [namespace]
    : ["common", "desktop", "web", "api"];
  for (const currentNamespace of namespaces) {
    const message =
      translations[locale][currentNamespace][key] ??
      translations[DEFAULT_LOCALE][currentNamespace][key];
    if (message !== undefined) {
      return interpolate(message, params);
    }
  }
  return interpolate(fallback ?? key, params);
}

export function translateText(
  text: string,
  localeInput: unknown,
  namespace: TranslationNamespace,
): string {
  const locale = normalizeLocale(localeInput);
  return legacyTranslations[namespace][text]?.[locale] ?? text;
}

export function interpolate(template: string, params: TranslationParams): string {
  return template.replace(/\{([a-zA-Z0-9_.-]+)\}/g, (match, key: string) => {
    const value = params[key];
    return value === undefined || value === null ? match : String(value);
  });
}
